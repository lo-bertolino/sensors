//leggi da AN0
unsigned int AN0Read(){
	/* per prova
	return 256;//*/
	//* impostazioni reali
	__delay_ms(1);
	GO_DONE=1;//convertitore on su canale 0
	while(GO_DONE);
	return ADRESH;//*/
}

unsigned int SevenSeg(unsigned int value){
    switch(value){
        case 0:
            return 0b00111111;
        case 1:
            return 0b00000110;
        case 2:
            return 0b01011011;
        case 3:
            return 0b01001111;
        case 4:
            return 0b01100110;
        case 5:
            return 0b01101101;
        case 6:
            return 0b01111100;
        case 7:
            return 0b00000111;
        case 8:
            return 0b01111111;
        case 9:
            return 0b01100111;
    }
}


int main() {
    Init();
    while(1){
        unsigned int decVal=(AN0Read()*80)/256;
        unsigned int unita=decVal%10;
        unsigned int decine=(decVal/10)%10;

        PORTC=SevenSeg (unita);

        if(decine)
            PORTB=SevenSeg (decine);
        else
            PORTB=0;
        __delay_ms(498);
    }
}
